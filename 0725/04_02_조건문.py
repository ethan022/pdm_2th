print("========== 13. 실전 3 : 메뉴 번호로 계산 선택 ==========")

a = 10
b = 5

# menu = int(input("연산 번호(1~4) 선택: "))
menu = int("4")                   # ← 입력 흉내

if menu == 1:
    print(a + b)                  # 1 → 더하기
elif menu == 2:
    print(a - b)                  # 2 → 빼기
elif menu == 3:
    print(a * b)                  # 3 → 곱하기
elif menu == 4:
    print(a / b)                  # → 2.0    (4 → 나누기)
else:
    print("잘못된 번호입니다")      # 1~4 가 아닌 모든 번호 처리


print()
print("========== 14. 리스트와 조건문 함께 쓰기 ==========")

# 04_01 의 리스트와 조건문을 붙이면 '데이터를 보고 판단'이 시작된다
temps = [25, 26, 24, 28, 91]

last = temps[-1]                  # 가장 최근 측정값
if last > 80:
    print("경고: 최근 온도", last, "도")     # → 경고: 최근 온도 91 도
else:
    print("최근 온도 정상:", last, "도")

if 999 in temps:                  # 이상값 포함 여부로 판단
    print("이상값 있음")
else:
    print("이상값 없음")            # → 이상값 없음


