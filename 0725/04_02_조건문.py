print("========== 1. 왜 조건문인가 ==========")

# ---------------------------------------------------------------------------
# 프로그램은 위에서 아래로 한 줄씩 '순서대로' 실행된다.
# 조건문은 이 흐름을 상황에 따라 '갈라지게' 만드는 도구다.
#
#   일상   : 비가 오면 우산을 챙기고, 안 오면 그냥 나간다
#   로그인 : 비밀번호가 맞으면 입장, 틀리면 재입력
#   설비   : 온도가 정상 범위를 벗어나면 경고, 정상이면 통과
#
# 갈림길의 기준이 되는 것이 02_03에서 배운 '조건식(비교의 결과 True/False)'이다.
# ---------------------------------------------------------------------------

print(10 > 3)                     # → True
temp = 85
print(temp > 80)                  # → True    (85는 80보다 크다)
temp = 70
print(temp > 80)                  # → False


print()
print("========== 2. if 문의 기본 구조 ==========")

# ---------------------------------------------------------------------------
# 형식:
#     if 조건식:              ← 콜론(:) = "여기부터 이 조건에 속하는 코드" 신호
#         실행할 코드          ← 들여쓰기(스페이스 4칸) = 조건에 속한 블록
#     그 다음 코드             ← 들여쓰기 밖 = 조건과 상관없이 항상 실행
# ---------------------------------------------------------------------------

temp = 85                         # 측정 온도
if temp > 80:                     # 만약 온도가 80을 넘으면
    print("온도 주의")             # → 온도 주의   (조건이 참이라 실행됨)
print("측정 종료")                 # → 측정 종료   (if와 상관없이 항상 실행)

print()

temp = 70                         # 이번엔 70으로
if temp > 80:                     # 70 > 80 → 거짓
    print("온도 주의")             # (건너뜀 — 출력 안 됨)
    print("점검 필요")             # (같은 블록 — 역시 건너뜀)
print("측정 종료")                 # → 측정 종료   (이것만 실행)


print()
print("========== 3. 들여쓰기 실수 두 가지 ==========")

# ---------------------------------------------------------------------------
# 실수 1) if 다음 줄을 들여 쓰지 않으면 → IndentationError (즉시 멈춰서 바로 알아챔)
#
#   if temp > 80:
#   print("온도 주의")        → IndentationError: expected an indented block
#
# 실수 2) '조용한 오작동' — 오류 없이 결과만 틀리는 게 더 무섭다!
# ---------------------------------------------------------------------------

temp = 70
if temp > 80:
    print("온도 주의")             # 조건 거짓 → 건너뜀
print("점검 필요")                 # → 점검 필요
# ↑ 원래 블록 안에 있어야 할 줄인데 들여쓰기가 풀리면
#   오류 없이 '항상' 실행돼 버린다. 결과만 틀려서 발견하기 어렵다.
#   블록에 넣을 줄인지 아닌지, 들여쓰기를 항상 의식할 것!


print()
print("========== 4. 경계 표현 ― 이상/초과/이하/미만 ==========")

# ---------------------------------------------------------------------------
#   우리말      기호      경계값(기준값 자체)
#   이상        >=        포함
#   초과        >         미포함
#   이하        <=        포함
#   미만        <         미포함
#
# "80 이상" 인데 > 를 쓰면 정확히 80일 때 판정이 틀린다. 경계에서 항상 확인!
# ---------------------------------------------------------------------------

age = 19
print(age >= 19)                  # → True    (19 '이상' — 19 포함)
print(age > 19)                   # → False   (19 '초과' — 19 미포함!)


print()
print("========== 5. if-else ― 두 갈래 ==========")

# else 는 if 조건이 거짓일 때 실행. 조건식을 따로 쓰지 않는다 (나머지 전부 담당).
# if 와 else 중 '항상 하나만' 실행된다.

score = 75
if score >= 60:                   # 75 >= 60 → 참
    print("합격")                  # → 합격    (이 줄만 실행)
else:
    print("불합격")                # (건너뜀)

age = 18
if age >= 19:
    print("성인입니다")            # (건너뜀)
else:
    print("미성년자입니다")         # → 미성년자입니다


print()
print("========== 6. if-elif-else ― 여러 갈래 ==========")

# ---------------------------------------------------------------------------
# elif = 갈림길이 셋 이상일 때 중간 조건 추가.
# 위에서부터 검사해서 '처음 참이 되는 곳 하나만' 실행하고 나머지는 건너뛴다.
# ---------------------------------------------------------------------------

score = 82
if score >= 90:                   # 82 >= 90 → 거짓
    print("우수")
elif score >= 70:                 # 82 >= 70 → 참!
    print("보통")                  # → 보통    (여기만 실행)
else:
    print("미흡")                  # (검사조차 안 함)

# 점수 등급 판정 (95→A, 85→B, 60→F 로 갈리는지 확인)
score = 85
if score >= 90:
    print("A")
elif score >= 80:
    print("B")                    # → B
elif score >= 70:
    print("C")
else:
    print("F")


print()
print("========== 7. ★함정★ elif 는 순서가 중요하다 ==========")

# ---------------------------------------------------------------------------
# '넓은 조건'을 위에 두면 좁은 조건이 영영 검사되지 않는다.
# ---------------------------------------------------------------------------

# 잘못된 순서: 60 이상(넓은 조건)이 맨 위
score = 95
if score >= 60:
    print("보통")                  # → 보통    ← 95점인데 보통?!
elif score >= 90:
    print("우수")                  # 95도 60 이상이라 첫 조건에서 걸려 여기 도달 못 함
# 95점이 '보통'이 되어버렸다. 오류도 안 난다!

# 올바른 순서: 좁고 까다로운 조건(90 이상)을 위에
score = 95
if score >= 90:
    print("우수")                  # → 우수    (제대로 판정)
elif score >= 60:
    print("보통")

# 규칙: 높은 기준 → 낮은 기준 순서로 배치


print()
print("========== 8. and · or · not 으로 조건 엮기 ==========")

# 02_03 에서 배운 논리 연산자를 조건문에서 본격 활용한다.
#   and : 둘 다 참일 때만    or : 하나라도 참이면    not : 뒤집기

temp = 45
if temp >= 20 and temp <= 60:
    print("정상 범위")             # → 정상 범위

# ★새 표기★ 범위 조건은 연쇄 비교로 더 짧게 쓸 수 있다
if 20 <= temp <= 60:              # temp >= 20 and temp <= 60 과 같은 뜻
    print("정상 범위")             # → 정상 범위

pressure = 8.5
if temp > 60 or pressure > 7:     # 하나라도 기준을 넘으면
    print("점검 필요")             # → 점검 필요   (압력 8.5 > 7)

is_running = False
if not is_running:                # 가동 중이 '아니면'
    print("설비 정지 상태")         # → 설비 정지 상태


print()
print("========== 9. = 와 == 혼동 주의 ==========")

#   =   대입 (값을 변수에 담기)         x = 5
#   ==  비교 (같은지 따지기)            x == 5
#
# if x = 5:   → SyntaxError!  조건식에는 반드시 == 를 쓴다.

x = 5                             # 대입
print(x == 5)                     # → True    (비교)


print()
print("========== 10. 조건문 코드 읽는 절차 ==========")

# ---------------------------------------------------------------------------
# ① 변수 값 확인  : 조건에 쓰인 변수의 현재 값을 위쪽에서 찾는다
# ② 조건 대입     : 위에서부터 값을 넣어 '처음 참'인 조건을 찾는다
# ③ 블록 추적     : 그 조건의 들여 쓴 코드만 따라간다
#
# 디버깅도 같은 절차: "주의"가 나와야 하는데 "정상"이 나온다?
#   → temp 값 확인 → 조건에 대입 → 비교 기호·순서·오타(80 vs 800) 추적
# ---------------------------------------------------------------------------


print("========== 11. 실전 1 : 설비 온도 3단계 판정 ==========")

# 실제 입력 버전 (주석을 풀면 input 으로 동작):
# temp = int(input("측정 온도를 입력하세요: "))

temp = int("85")                  # ← input("측정 온도: ") 흉내 (결과는 글자 → int 변환)
if temp > 80:
    print("위험")                  # → 위험
elif temp > 60:
    print("주의")
else:
    print("정상")

# 85 → 위험 / 70 → 주의 / 50 → 정상 으로 갈리는지 값을 바꿔 확인해 볼 것


