print("========== 4. 인덱싱 ― 순서 번호로 값 하나 꺼내기 ==========")

# ---------------------------------------------------------------------------
# 문자열 인덱싱과 완전히 같은 규칙!
#   · 첫 번째 값의 번호는 0
#   · 마지막 인덱스 = 개수 - 1
#   · 음수는 뒤에서부터 (-1 이 마지막)
#
#     값     25   26   24   28   27
#     양수    0    1    2    3    4
#     음수   -5   -4   -3   -2   -1
# ---------------------------------------------------------------------------

temps = [25, 26, 24, 28, 27]
print(temps[0])                        # → 25   (첫 번째)
print(temps[2])                        # → 24   (세 번째)
print(temps[-1])                       # → 27   (마지막)
print(temps[-2])                       # → 28   (뒤에서 두 번째)

# 없는 자리를 가리키면 오류 (주석 처리)
# print(temps[5])                      → IndexError: list index out of range
#   5개짜리 리스트의 인덱스는 0~4까지. 개수보다 하나 작다!


