print("========== 9. 값 추가 ― append / insert / extend ==========")

# ---------------------------------------------------------------------------
#   append(값)        맨 끝에 하나 추가
#   insert(위치, 값)   그 위치에 끼워 넣기 (뒤 값들은 한 칸씩 밀림)
#   extend(리스트)     다른 리스트의 값들을 풀어서 이어붙임
#
# ★ 주의: 문자열 메서드와 달리 리스트 메서드는 '원본을 직접 바꾼다'!
#         결과를 다시 받을 필요가 없다. (아래 12번에서 자세히)
# ---------------------------------------------------------------------------

temps = [25, 26, 24]
temps.append(28)                       # 끝에 추가
print(temps)                           # → [25, 26, 24, 28]
temps.append(27)
print(temps)                           # → [25, 26, 24, 28, 27]

temps = [25, 26, 24, 27]
temps.insert(2, 99)                    # 인덱스 2 자리에 끼워 넣기
print(temps)                           # → [25, 26, 99, 24, 27]

morning = [25, 26, 24]
afternoon = [28, 27, 30]
morning.extend(afternoon)              # 리스트 통째로 이어붙이기
print(morning)                         # → [25, 26, 24, 28, 27, 30]

# 빈 리스트에서 시작해 쌓아가기 (데이터 수집의 기본 패턴)
collected = []
collected.append(30)
print(collected)                       # → [30]
collected.insert(0, 28)                # 맨 앞에 끼워 넣기
print(collected)                       # → [28, 30]
collected.extend([31, 32])
print(collected)                       # → [28, 30, 31, 32]


