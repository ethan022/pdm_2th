print("========== 12. ★흔한 실수★ 리스트 메서드는 원본을 바꾼다 ==========")

# ---------------------------------------------------------------------------
# 문자열(03_04): 원본이 안 바뀜  →  s = s.upper() 로 다시 받아야 했다
# 리스트(04_01): 원본이 바뀜!   →  temps.sort() 만 쓰면 끝. 다시 받으면 오히려 사고!
# ---------------------------------------------------------------------------

temps = [27, 24, 30]
temps.sort()                           # 이걸로 충분 (원본이 이미 정렬됨)
print(temps)                           # → [24, 27, 30]

temps = [27, 24, 30]
result = temps.sort()                  # 문자열 습관대로 다시 받으면?
print(result)                          # → None    ← 아무것도 안 돌려준다!
print(temps)                           # → [24, 27, 30]  (정렬은 원본에 이미 반영)

# "리스트가 None 이 됐어요!" 는 대부분 temps = temps.sort() 를 쓴 경우다.
# 문자열 메서드는 '새 결과를 돌려주고', 리스트 메서드는 '원본을 고친다'.


