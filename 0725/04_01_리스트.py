print("========== 13. 종합 : 측정 데이터 정리 ==========")

# 생성 → 확인 → 수정 → 추가 → 정렬을 하나의 흐름으로
temps = [25, 26, 999, 24, 28, 26, 27, 23]

print(999 in temps)                    # → True     이상값이 있나?
temps.remove(999)                      # 있으면 제거

print(temps.count(26))                 # → 2        중복이 있나?
temps.remove(26)                       # 하나만 제거 (첫 번째 것)

temps.append(29)                       # 새 측정값 추가
temps.sort()                           # 정렬
print(temps)                           # → [23, 24, 25, 26, 27, 28, 29]

# 정렬된 리스트의 첫 값 = 최솟값, 마지막 값 = 최댓값!
print(temps[0], temps[-1])             # → 23 29


