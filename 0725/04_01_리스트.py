print("========== 6. 슬라이싱 ― 구간 잘라내기 ==========")

# ---------------------------------------------------------------------------
# 이것도 문자열과 같은 규칙: 시작 포함, 끝 제외. 결과는 '새 리스트'.
# ---------------------------------------------------------------------------

temps = [25, 26, 24, 28, 27]
#         0   1   2   3   4
print(temps[1:4])                      # → [26, 24, 28]   (1,2,3번 / 4 제외)
print(temps[:3])                       # → [25, 26, 24]   (처음부터)
print(temps[2:])                       # → [24, 28, 27]   (끝까지)
print(temps[:])                        # → [25, 26, 24, 28, 27]  (전체)
print(temps[-3:])                      # → [24, 28, 27]   (뒤에서 3개)

# step ― 건너뛰며 가져오기
temps6 = [25, 26, 24, 28, 27, 23]
print(temps6[0:6:2])                   # → [25, 24, 27]   (2칸씩)
print(temps6[::2])                     # → [25, 24, 27]   (전체에서 2칸씩)

# 데이터를 앞·뒤 두 구간으로 나누기
