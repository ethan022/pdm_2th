print("========== 8. in ― 값이 들어 있는지 확인 ==========")

machines = ["펌프", "모터", "압축기"]
print("모터" in machines)               # → True
print("밸브" in machines)               # → False
print("밸브" not in machines)           # → True

# 활용: 잘못 들어온 값(240)을 찾아 바로잡기
temps = [25, 26, 240, 28, 27]
print(240 in temps)                    # → True    (이상값이 있다!)
i = temps.index(240)                   # 처음 나오는 위치를 찾아서
print(i)                               # → 2
temps[i] = 24                          # 그 자리를 올바른 값으로 교체
print(temps)                           # → [25, 26, 24, 28, 27]
print(240 in temps)                    # → False   (사라짐 확인)


