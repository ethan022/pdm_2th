print("========== 10. 값 제거 ― remove / pop / del ==========")

# ---------------------------------------------------------------------------
#   remove(값)    그 '값'과 같은 첫 번째 항목을 제거
#   pop(위치)     그 '위치'의 값을 꺼내 돌려주면서 제거
#   del 리스트[위치]   위치로 삭제, 돌려주지 않음 (점 없이 쓰는 명령)
#
#   기준이 '값'이면 remove, '위치'면 pop 또는 del.
#   지운 값을 써야 하면 pop.
# ---------------------------------------------------------------------------

machines = ["펌프", "모터", "압축기", "모터"]
machines.remove("모터")                 # 값으로 제거 (첫 번째 것만!)
print(machines)                        # → ['펌프', '압축기', '모터']

temps = [25, 26, 24, 28, 27]
removed = temps.pop(2)                 # 인덱스 2를 꺼내며 제거
print(removed)                         # → 24    (꺼낸 값을 돌려받음)
print(temps)                           # → [25, 26, 28, 27]

temps = [25, 26, 24, 28, 27]
del temps[2]                           # 인덱스 2 삭제
print(temps)                           # → [25, 26, 28, 27]

temps = [25, 26, 24, 28, 27]
del temps[1:3]                         # 구간 삭제도 가능
print(temps)                           # → [25, 28, 27]

# 흐름 연습: 이상값 999 정리
temps = [25, 26, 999, 24, 28, 26]
temps.remove(999)                      # 값 기준 제거
print(temps)                           # → [25, 26, 24, 28, 26]
x = temps.pop(1)                       # 위치 기준, 꺼내면서
print(x)                               # → 26
del temps[0]                           # 위치 기준, 그냥 삭제
print(temps)                           # → [24, 28, 26]


