print("========== 11. 정렬과 탐색 ― sort / reverse / count / index ==========")

temps = [27, 24, 30, 22, 26]
temps.sort()                           # 작은 값부터 (오름차순)
print(temps)                           # → [22, 24, 26, 27, 30]
temps.sort(reverse=True)               # 큰 값부터 (내림차순)
print(temps)                           # → [30, 27, 26, 24, 22]

temps = [25, 30, 22, 28]
temps.reverse()                        # 크기와 무관하게 현재 순서를 뒤집기
print(temps)                           # → [28, 22, 30, 25]

results = ["정상", "이상", "정상", "정상", "이상"]
print(results.count("정상"))            # → 3   (몇 개 들어있나)
print(results.index("이상"))            # → 1   (처음 나오는 위치)


