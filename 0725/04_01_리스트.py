print("========== 5. 인덱스로 꺼낸 값 계산하기 ==========")

output = [120, 95, 130, 110, 88, 102]
first = output[0]                      # 첫 값을 변수에 담고
last = output[-1]                      # 마지막 값도 담아서
print(first + last)                    # → 222     (계산에 사용)
print((first + last) / 2)              # → 111.0   (두 값의 중간값)


