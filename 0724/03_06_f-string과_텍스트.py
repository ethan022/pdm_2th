#  배운 것 : f-string 기본, 중괄호 안 계산, 소수점 자리 지정(:.2f),
#            흔한 실수(f 빠뜨림), 텍스트 정리 5단계 흐름과 실전 사례
#  사용법  : "→" 결과를 먼저 예상한 뒤 실행해서 맞춰보기
# =============================================================================

print("========== 1. 왜 f-string 인가 ==========")

# ---------------------------------------------------------------------------
# 숫자·글자를 섞은 문장을 + 로 만들면:
#   · 따옴표와 더하기가 남발되고
#   · 숫자마다 str() 로 감싸야 하고
#   · 완성될 문장이 코드만 봐선 한눈에 안 들어온다
# 이 불편을 푸는 도구가 f-string 이다.
# ---------------------------------------------------------------------------

name = "PUMP_A"
temp = 87

# 기존 방식 (03_01에서 배운 + 연결)
print("설비 " + name + ", 온도 " + str(temp) + "도")     # → 설비 PUMP_A, 온도 87도

# f-string 방식 — 완성 문장 그대로 쓰고 변수만 중괄호로
print(f"설비 {name}, 온도 {temp}도")                     # → 설비 PUMP_A, 온도 87도
# 같은 결과인데 훨씬 짧고, str() 도 필요 없다.
# 실무에서 문장을 만들 땐 거의 항상 f-string 을 쓴다.


print()
print("========== 2. f-string 기본 문법 ==========")

# ---------------------------------------------------------------------------
# 형식:  f"...{변수}..."
#   · 따옴표 '앞'에 f  ← 중괄호를 변수로 해석하라는 신호
#   · 변수 자리에 중괄호 {변수}
#   · 중괄호 바깥 글자는 그대로 출력
#   · 주의: 중괄호 안에는 변수 '이름'만 (따옴표 없이!)
# ---------------------------------------------------------------------------

name = "홍길동"
age = 25
print(f"{name}님은 {age}살입니다")        # → 홍길동님은 25살입니다

code = "EQP-001"
print(f"설비 {code} 점검 완료")           # → 설비 EQP-001 점검 완료

# 숫자를 str() 로 안 바꿔도 알아서 끼워진다 (자동 형변환)
print(f"온도: {25}")                     # → 온도: 25
print(f"측정값: {2.5}")                  # → 측정값: 2.5


print()
print("========== 3. 중괄호 안에서 계산하기 ==========")

# 중괄호 안에 계산식을 넣으면 '계산 결과'가 문장에 들어간다.
hours = 2
print(f"{hours}시간 = {hours * 60}분")    # → 2시간 = 120분

a = 80
b = 91
c = 90
print(f"평균 {(a + b + c) / 3}")         # → 평균 87.0

temp = 85
print(f"기준 초과? {temp > 80}")          # → 기준 초과? True   (비교 결과도 OK)

# 복잡한 계산은 미리 변수에 담는 게 읽기 좋다
avg = (a + b + c) / 3
print(f"평균 {avg}")                     # → 평균 87.0


print()
print("========== 4. 소수점 자리 지정 :.Nf ==========")

# ---------------------------------------------------------------------------
# 형식:  {값:.2f}   ← 콜론 뒤에 .자릿수f
#   · :.1f  첫째 자리까지   :.2f  둘째 자리까지   :.3f  셋째 자리까지
#   · 자동으로 '반올림'된다
#   · 측정값·평균·비율을 보기 좋게 정리할 때 유용
# ---------------------------------------------------------------------------

value = 25.34567
print(f"측정값 {value}")                  # → 측정값 25.34567
print(f"측정값 {value:.2f}")              # → 측정값 25.35    (반올림!)
print(f"측정값 {value:.1f}")              # → 측정값 25.3

rate = 87.456
print(f"{rate:.1f}")                     # → 87.5
print(f"{rate:.2f}")                     # → 87.46

# 02_03 에서 지저분하게 나오던 값도 깔끔하게
run_h = 19
all_h = 24
print(f"가동률 {run_h / all_h * 100}%")       # → 가동률 79.16666666666666%
print(f"가동률 {run_h / all_h * 100:.1f}%")   # → 가동률 79.2%


print()
print("========== 5. 흔한 실수 ― f 빠뜨림 ==========")

# ---------------------------------------------------------------------------
#   증상: 중괄호와 변수 이름이 '글자 그대로' 출력된다
#   원인: 따옴표 앞에 f 를 빠뜨림
#   진단: 따옴표 바로 앞에 f 가 있는지 먼저 확인
#   또 하나: 중괄호 안에 따옴표를 넣는 실수 → {name} 이지 {"name"} 이 아니다
# ---------------------------------------------------------------------------

name = "센서A"
print("이름은 {name}")                    # → 이름은 {name}    (f가 없으면 그냥 글자!)
print(f"이름은 {name}")                   # → 이름은 센서A


print()
print("========== 6. 텍스트 정리 도구 총정리 ==========")

# ---------------------------------------------------------------------------
# 이번 파트(03장)의 도구를 네 묶음으로 정리
#
#   목적          도구
#   ------------------------------------------------------
#   만들기·출력    따옴표, print, f-string
#   형변환        str, int, float
#   꺼내기        인덱싱, 슬라이싱, split
#   확인·다듬기    len·in·count·find / strip·lower·replace·join
#
# 텍스트 정리 5단계 흐름 (필요한 단계만 골라 쓰면 된다)
#
#   공백제거   →   통일·치환      →   나누기   →   조각정리     →   합치기
#   strip          lower·replace     split        조각 strip       join·출력
# ---------------------------------------------------------------------------


print("========== 7. 사례 1 : 이메일 정리 ==========")

# 공백 제거 후 소문자 통일이 기본
email = "  Hong@Company.com  "
clean = email.strip().lower()
print(clean)                             # → hong@company.com

# 아이디는 find 로 @ 위치를 찾아 슬라이싱 (03_03 복습)
at = clean.find("@")
print(f"아이디: {clean[:at]}")            # → 아이디: hong
print(f"도메인: {clean[at + 1:]}")        # → 도메인: company.com


print()
print("========== 8. 사례 2 : 전화번호 정리 ==========")

# 하이픈·공백을 replace 로 제거해 숫자만 남기기
phone = "010 - 1234 - 5678"
digits = phone.replace("-", "").replace(" ", "")
print(digits)                            # → 01012345678
print(f"자릿수: {len(digits)}")           # → 자릿수: 11   (len 으로 11자리 점검)

# 다시 형식을 넣을 땐 슬라이싱 + f-string
print(f"{digits[:3]}-{digits[3:7]}-{digits[7:]}")     # → 010-1234-5678


print()
print("========== 9. 사례 3 : 이름 정리 ==========")

# 공백 제거 후 title 로 형식 통일
name = "  hong gil dong  "
print(name.strip().title())              # → Hong Gil Dong

# 공백이 들쭉날쭉하면 split 후 한 칸으로 join
messy = "hong   gil    dong"
parts = messy.split()                    # 공백 기준 → 여러 칸도 알아서 처리
fixed = " ".join(parts)
print(fixed)                             # → hong gil dong
print(fixed.title())                     # → Hong Gil Dong


print()
print("========== 10. 사례 4 : 쉼표 구분 목록 정리 ==========")

# split → 각 항목 strip → join
raw = "사과, 배 , 감"
parts = raw.split(",")
print(parts)                             # → ['사과', ' 배 ', ' 감']

# 각 조각을 번호로 꺼내 정리한 뒤 다시 합친다 (리스트 자세한 건 다음 단원)
cleaned = [parts[0].strip(), parts[1].strip(), parts[2].strip()]
print(cleaned)                           # → ['사과', '배', '감']
print(", ".join(cleaned))                # → 사과, 배, 감


print()
print("========== 11. 사례 5 : 컬럼명 정리 ==========")

# 공백 제거 · 소문자 · 공백을 밑줄로 — 다음 단원 데이터 정리에서 그대로 쓴다
col = " Sensor Measurement 3 "
clean = col.strip().lower().replace(" ", "_")
print(clean)                             # → sensor_measurement_3


print()
print("========== 12. 사례 6 : 범주형 라벨 통일 ==========")

# 정해진 몇 종류로 나뉘는 라벨은 표기 통일이 핵심 (03_04 복습 + replace)
a = "NORMAL"
b = " normal "
c = "Normal"
print(a.strip().lower())                 # → normal
print(b.strip().lower())                 # → normal
print(c.strip().lower())                 # → normal

# replace 로 표기 자체를 맞출 수도 있다
status = "fault"
print(status.replace("fault", "고장"))    # → 고장


print()
print("========== 13. 여러 항목 한 줄 요약 출력 ==========")

# 정리한 값들을 f-string 한 줄로 모아 요약.
# 중괄호 '바깥' 기호([, /, :)는 양식 그대로 출력된다.
code = "EQP-001"
status = "warning"
value = 95.2345
print(f"[{code}] 상태: {status} / 측정값: {value:.1f}")
# → [EQP-001] 상태: warning / 측정값: 95.2

# 분석의 마지막은 숫자를 '사람이 읽는 문장'으로 바꾸는 일이다.
# 이 과정 최종 산출물인 정비 판단 문장이 바로 이 형태다.


print()
print("========== 14. 종합 : 센서 로그 한 줄 정리 리포트 ==========")

# ---------------------------------------------------------------------------
# 지저분한 로그 한 줄 → 정리 → f-string 리포트   (이번 파트의 완성형!)
#   정리는 앞 모듈 도구(strip/split/lower/float)가,
#   출력은 f-string 이 담당한다.
# ---------------------------------------------------------------------------

raw = "  5 , sensor_2 , WARNING , 0.78912  "

parts = raw.strip().split(",")           # ① 앞뒤 공백 제거 후 쉼표로 분리
print(parts)                             # → ['5 ', ' sensor_2 ', ' WARNING ', ' 0.78912']

sid = parts[1].strip()                   # ② 조각별 정리
status = parts[2].strip().lower()
value = float(parts[3].strip())          # ③ 측정값은 숫자로

print(sid)                               # → sensor_2
print(status)                            # → warning
print(value)                             # → 0.78912

# ④ f-string 으로 한 줄 판단 리포트
print(f"[센서 {sid}] 상태 {status}, 측정값 {value:.2f}")
# → [센서 sensor_2] 상태 warning, 측정값 0.79


