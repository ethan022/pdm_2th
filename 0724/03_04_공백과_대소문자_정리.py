print("========== 1. 메서드란 무엇인가 ==========")

# ---------------------------------------------------------------------------
# 값 하나하나가 '객체' = 값(데이터) + 그 값에 딸린 기능.
# 자료형은 '객체의 종류'라서, 종류마다 쓸 수 있는 기능이 다르다.
#   → 문자열엔 문자열 기능(대문자 바꾸기, 공백 떼기 …)
#   → 숫자엔 '대문자로 바꾸기' 같은 기능이 아예 없다
#
# 메서드 = 특정 자료형(값=객체)에 소속된 함수.
#
# 형식:  값.메서드이름()
#        · 점(.)을 찍어 부른다
#        · 괄호를 꼭 붙여야 실행된다
#        · 추가 정보가 필요하면 괄호 안에 넣는다   예) count("a")
# ---------------------------------------------------------------------------

word = "python"
print(word.upper())               # → PYTHON
print(word.count("p"))            # → 1
print(word.startswith("p"))       # → True

# 괄호를 빼면 실행되지 않고 '기능 그 자체'가 출력된다 (흔한 실수)
# print(word.upper)               → <built-in method upper of str object at ...>


print()
print("========== 2. ★가장 중요★ 원본은 바뀌지 않는다 ==========")

# ---------------------------------------------------------------------------
# 문자열 메서드는 원본을 바꾸지 않고 '새 결과'를 돌려준다.
# 그래서 결과를 쓰려면 반드시 변수에 다시 받아야 한다.
#
#        word = word.upper()      ← 이 패턴을 통째로 외우자
#
# 이걸 놓치면 "분명 바꿨는데 값이 그대로다" 하는 상황이 계속 생긴다.
# ---------------------------------------------------------------------------

s = "python"
s.upper()                         # 결과를 안 받으면 그냥 버려진다
print(s)                          # → python    (바뀐 게 없다!)

s = s.upper()                     # 다시 받아야 반영된다
print(s)                          # → PYTHON

# 새 변수에 받아도 된다 (원본을 남겨두고 싶을 때)
raw = "python"
big = raw.upper()
print(raw)                        # → python    (원본 그대로)
print(big)                        # → PYTHON    (결과는 새 변수에)


print()
print("========== 3. upper() / lower() ==========")

# 영문 글자를 모두 대문자 / 소문자로. 한글·숫자·기호는 그대로 둔다.
print("python".upper())           # → PYTHON
print("NORMAL".lower())           # → normal
print("정상 normal".upper())       # → 정상 NORMAL   (한글은 그대로)
print("EQP-001".lower())          # → eqp-001       (숫자·기호는 그대로)

s = "ready"
big = s.upper()
print(big)                        # → READY

s = "WARNING"
small = s.lower()
print(small)                      # → warning

# 실무에서는 lower 를 더 자주 쓴다 (소문자로 통일하는 게 관례)


print()
print("========== 4. 대소문자 통일이 필요한 이유 ==========")

# ---------------------------------------------------------------------------
# 같은 상태인데 표기만 다른 데이터가 한 파일에 섞여 들어온다.
#     'NORMAL'  'Normal'  'normal'
# 통일하지 않으면 컴퓨터는 이를 서로 다른 세 상태로 보고 따로 집계한다.
# → 보통 lower() 로 모두 소문자로 맞춘다.
# ---------------------------------------------------------------------------

a = "Fault"
b = "FAULT"
print(a == b)                     # → False   (대소문자가 달라 다른 값)
print(a.lower() == b.lower())     # → True    (소문자로 통일한 뒤 비교)

# 이 패턴은 데이터를 다룰 때 표준처럼 쓰인다
print("NORMAL".lower() == "normal".lower())   # → True
print("Normal".lower() == "normal")           # → True


print()
print("========== 5. capitalize() / title() ==========")

# capitalize() : 문장의 '첫 글자'만 대문자
# title()      : '단어마다' 첫 글자를 대문자
print("python is fun".capitalize())     # → Python is fun
print("python is fun".title())          # → Python Is Fun

name = "kim chul soo"
print(name.title())                     # → Kim Chul Soo

# 주의: capitalize() 는 첫 글자만 올리고 '나머지는 전부 소문자로 내린다'
print("PYTHON IS FUN".capitalize())     # → Python is fun   (뒤가 소문자가 됨!)

# 주의: title() 은 따옴표·기호 뒤도 단어의 시작으로 본다
print("it's normal".title())            # → It'S Normal     (S가 대문자!)
#   → 사람 이름·제목 정리에 '가끔' 쓰는 용도. 만능이 아니다.


print()
print("========== 6. isupper() / islower() ― 바꾸지 않고 '검사' ==========")

# ---------------------------------------------------------------------------
# is 로 시작하는 메서드는 대체로 "~인지 확인"하는 검사용.
# 바꾸는 게 아니라 True / False 를 돌려준다.
# ---------------------------------------------------------------------------
print("ABC".isupper())            # → True
print("abc".islower())            # → True
print("Abc".isupper())            # → False   (섞여 있으면 False)
print("Abc".islower())            # → False

# 수업에서 본 예시
a = "Abc"
b = "abc"
c = "ABC"
print(a.isupper(), a.islower())   # → False False
print(b.isupper(), b.islower())   # → False True
print(c.isupper(), c.islower())   # → True False

# 참고: 숫자·기호는 판단 대상이 아니다. 영문 글자만 보고 판단한다.
print("ABC1".isupper())           # → True    (숫자는 무시하고 A,B,C 만 봄)
print("123".isupper())            # → False   (판단할 영문 글자가 하나도 없음)


print()
print("========== 7. strip() ― 앞뒤 공백 제거 ==========")

# ---------------------------------------------------------------------------
# 눈에 안 보이는 앞뒤 공백이 데이터를 다른 값으로 만든다.
#     '정상' 과 '정상 '(뒤 공백)은 다른 값 → 같은 상태가 따로 집계된다.
# 그래서 데이터를 다루기 전 앞뒤 공백 제거는 거의 필수다.
#
# 공백은 눈으로 안 보이니 [ ] 로 감싸서 확인하는 습관을 들이자.
# ---------------------------------------------------------------------------

text = "  정상  "
print("[" + text + "]")           # → [  정상  ]
text = text.strip()               # ← 결과를 다시 받는 것 잊지 말기
print("[" + text + "]")           # → [정상]

s = "  가동중  "
clean = s.strip()
print("[" + clean + "]")          # → [가동중]

# 공백 차이가 만드는 문제
print("정상" == "정상 ")           # → False   (뒤 공백 하나 차이)
print("정상" == "정상 ".strip())   # → True    (정리하면 같아진다)


print()
print("========== 8. lstrip() / rstrip() ― 한쪽만 제거 ==========")

# l = left(왼쪽·앞),  r = right(오른쪽·뒤)
# 보통은 strip() 하나로 양쪽 다 떼면 충분하고,
# 한쪽 공백을 살려야 할 때만 쓴다.

s = "  대기  "
print("[" + s.lstrip() + "]")     # → [대기  ]   (왼쪽만 제거)
print("[" + s.rstrip() + "]")     # → [  대기]   (오른쪽만 제거)
print("[" + s.strip() + "]")      # → [대기]     (양쪽 다)


print()
print("========== 9. strip(문자) ― 특정 문자 제거 ==========")

# ---------------------------------------------------------------------------
# 괄호에 글자를 넣으면 공백 대신 '그 글자'를 양 끝에서 제거한다.
# 더 이상 없을 때까지 양 끝에서 계속 떼어낸다.
# 주의: 가운데 글자는 건드리지 않는다.
# ---------------------------------------------------------------------------

print("###정상###".strip("#"))     # → 정상
print("***경고***".strip("*"))     # → 경고

# 여러 글자를 넣으면 '그 중 아무거나' 양 끝에서 제거한다 (순서와 무관)
print("#$#대기#$#".strip("#$"))    # → 대기
print("#$!경보!$#".strip("#$!"))   # → 경보

# 가운데는 안 지운다는 것 확인
print("##정상##상태##".strip("#")) # → 정상##상태


print()
print("========== 10. strip 으로 못 지우는 중간 공백 ==========")

# strip 은 '앞뒤 전용'이다. 글자 사이의 공백은 못 지운다.
s = "  정 상  "
print("[" + s.strip() + "]")      # → [정 상]   (가운데 공백은 그대로!)

# 중간 공백은 다음 시간에 배울 replace 로 해결한다
# print(s.strip().replace(" ", ""))   → 정상


print()
print("========== 11. 체이닝 ― 메서드 이어 붙이기 ==========")

# ---------------------------------------------------------------------------
# 메서드 뒤에 또 메서드를 점으로 이어 붙일 수 있다.
# 읽는 순서는 왼쪽에서 오른쪽. 정리 단계가 여러 개일 때 코드가 깔끔해진다.
#
#   raw.strip().lower()
#    └ 공백 제거 → 그 결과를 소문자로
# ---------------------------------------------------------------------------

raw = "  NORMAL  "
step1 = raw.strip()               # 'NORMAL'
print("[" + step1 + "]")          # → [NORMAL]

clean = raw.strip().lower()       # 두 단계를 한 줄에
print(clean)                      # → normal

s = "  WARNING  "
print(s.strip().lower())          # → warning

# 입력값은 보통 strip → lower 순으로 정리한다.
# "입력값이 들어오면 일단 정리부터" 를 습관으로.


print()
print("========== 12. 흔한 실수 ― 결과를 저장 안 함 ==========")

# ---------------------------------------------------------------------------
#   Q. text.strip() 만 쓰고 끝내면?
#   A. 정리된 결과가 그냥 사라지고 원본은 그대로다.
#
#   Q. 어떻게 고치나?
#   A. text = text.strip() 으로 결과를 다시 받는다.
#
#   Q. "분명 바꿨는데 값이 안 바뀐 것 같다" 싶으면?
#   A. 결과를 다시 받았는지 가장 먼저 확인한다.
# ---------------------------------------------------------------------------

s = "  경보  "
s.strip()                         # 결과를 안 받음 → 버려진다
print("[" + s + "]")              # → [  경보  ]   (변화 없음)

s = s.strip()                     # 다시 받아야 반영
print("[" + s + "]")              # → [경보]


print()
print("========== 13. 실전 : 파일명 규칙 점검 ==========")

# 대소문자가 뒤죽박죽인 파일명도 lower() 로 통일하면 한 번에 점검할 수 있다
fname = "Sensor_LOG.CSV"

print(fname.endswith(".csv"))        # → False   (대문자 .CSV 라 통일 전엔 False)

low = fname.lower()                  # sensor_log.csv
print(low)                           # → sensor_log.csv
print(low.startswith("sensor"))      # → True
print(low.endswith(".csv"))          # → True

# 체이닝으로 한 줄에
print(fname.lower().endswith(".csv"))    # → True


print()
print("========== 14. 실전 : 입력값 정리 ==========")

# 사용자·파일에서 들어온 지저분한 값들을 표준 형태로 정리
raw1 = "  NORMAL  "
raw2 = "Normal"
raw3 = "normal  "

clean1 = raw1.strip().lower()
clean2 = raw2.strip().lower()
clean3 = raw3.strip().lower()

print("[" + clean1 + "]")            # → [normal]
print("[" + clean2 + "]")            # → [normal]
print("[" + clean3 + "]")            # → [normal]

# 정리 전에는 셋 다 다른 값
print(raw1 == raw2)                  # → False
print(raw2 == raw3)                  # → False

# 정리 후에는 모두 같은 값 → 정확한 비교·집계가 가능해진다
print(clean1 == clean2)              # → True
print(clean2 == clean3)              # → True

print()

# 설비 상태 라벨 정리
label = "  WARNING  "
clean = label.strip().lower()
print("원본:", "[" + label + "]")     # → 원본: [  WARNING  ]
print("정리:", "[" + clean + "]")     # → 정리: [warning]
print("경고인가?", clean == "warning")  # → 경고인가? True


