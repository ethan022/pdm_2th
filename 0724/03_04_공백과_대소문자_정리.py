#  정리 ― 오늘 배운 메서드
# -----------------------------------------------------------------------------
#   공통 원칙 : 원본은 안 바뀐다. 결과를 반드시 다시 받는다.
#
#   대소문자   upper()        모두 대문자
#              lower()        모두 소문자          ← 가장 자주 씀
#              capitalize()   문장 첫 글자만 대문자 (나머지는 소문자로)
#              title()        단어마다 첫 글자 대문자
#              isupper()      모두 대문자인지 검사 (True/False)
#              islower()      모두 소문자인지 검사 (True/False)
#
#   공백       strip()        앞뒤 공백 제거       ← 가장 자주 씀
#              lstrip()       왼쪽(앞)만
#              rstrip()       오른쪽(뒤)만
#              strip("문자")   앞뒤에서 그 글자를 제거
#
#   체이닝     text.strip().lower()   점으로 이어 붙이기 (왼쪽 → 오른쪽 순서)
# =============================================================================

