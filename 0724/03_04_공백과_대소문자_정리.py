#  배운 것 : 메서드와 점(.) 문법, 원본은 안 바뀐다는 원칙,
#            upper / lower / capitalize / title / isupper / islower,
#            strip / lstrip / rstrip / strip(문자), 체이닝
#  사용법  : "→" 결과를 먼저 예상한 뒤 실행해서 맞춰보기
