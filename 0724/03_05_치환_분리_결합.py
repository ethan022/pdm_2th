print("========== 1. replace() ― 단어 바꾸기 ==========")

# ---------------------------------------------------------------------------
# 형식:  문자열.replace("바꿀것", "새것")
#   · 등장하는 것을 '모두 한 번에' 바꾼다
#   · 원본은 안 바뀜 → 결과를 변수에 다시 받기 (03_04 공통 원칙 그대로)
# ---------------------------------------------------------------------------

s = "설비 정상 가동"
print(s.replace("정상", "점검"))       # → 설비 점검 가동
print(s)                              # → 설비 정상 가동   (원본은 그대로!)

s = s.replace("정상", "점검")          # 다시 받아야 반영
print(s)                              # → 설비 점검 가동

# '모두' 바꾼다는 것 확인
t = "고장 신호 고장 경보"
print(t.replace("고장", "정상"))       # → 정상 신호 정상 경보  (두 곳 다 바뀜)


print()
print("========== 2. replace 로 공백·기호 제거 ==========")

# ---------------------------------------------------------------------------
# 바꿀 대상을 빈 값("")으로 바꾸면 그 글자가 '사라진다'.
# strip 이 못 하던 '중간 공백'을 replace 가 해결한다!
#   strip   → 앞뒤 전용
#   replace → 위치와 상관없이 전부
# ---------------------------------------------------------------------------

text = "정 상 가 동"
text = text.replace(" ", "")          # 공백을 빈 값으로 = 공백 제거
print(text)                           # → 정상가동

phone = "010-1234-5678".replace("-", "")
print(phone)                          # → 01012345678

# 03_04 에서 남겨뒀던 문제 해결
s = "  정 상  "
print("[" + s.strip() + "]")               # → [정 상]   strip은 가운데를 못 지움
print("[" + s.replace(" ", "") + "]")      # → [정상]    replace는 전부 지움


print()
print("========== 3. replace 여러 번 연결 (체이닝) ==========")

# 앞 결과에 다음 replace 를 바로 적용. 왼쪽부터 차례로 정리된다.
messy = "010 - 1234 - 5678"
clean = messy.replace("-", "").replace(" ", "")
print(clean)                          # → 01012345678


