print("========== 8. split(구분자, 횟수) ― 나누는 횟수 제한 ==========")

# 횟수를 함께 넣으면 앞에서부터 그만큼만 나눈다. 자주 쓰진 않는 옵션.
s = "a,b,c,d"
print(s.split(",", 1))                # → ['a', 'b,c,d']   (첫 쉼표에서 한 번만)


print()
print("========== 9. split 활용 ― CSV 한 줄 나누기 ==========")

# CSV(쉼표로 값을 나눈 파일)의 한 줄을 split(',') 으로 나눠 각 값 꺼내기
row = "1,EQP-001,정상,25.3"
parts = row.split(",")
print(parts)                          # → ['1', 'EQP-001', '정상', '25.3']
print("사이클:", parts[0])             # → 사이클: 1
print("코드:", parts[1])               # → 코드: EQP-001
print("상태:", parts[2])               # → 상태: 정상

value = float(parts[3])               # 측정값은 아직 글자 → float 로 변환
print("측정값:", value)                # → 측정값: 25.3
print("측정값 x 2:", value * 2)        # → 측정값 x 2: 50.6


