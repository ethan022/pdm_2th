print("========== 5. 리스트 맛보기 (split 결과 이해용) ==========")

# ---------------------------------------------------------------------------
# 리스트 = 여러 값을 순서대로 담는 그릇. 대괄호 안에 쉼표로 나열한다.
# 번호로 조각 하나를 꺼낸다 (0부터 시작 — 문자열 인덱싱과 동일!)
# 자세한 건 다음 단원(04_01). 지금은 split 결과를 읽기 위한 맛보기.
# ---------------------------------------------------------------------------

fruits = ["사과", "배", "감"]
print(fruits)                         # → ['사과', '배', '감']
print(fruits[0])                      # → 사과   (0번)
print(fruits[2])                      # → 감    (2번)
print(fruits[-1])                     # → 감    (음수 인덱스도 동일)


print()
print("========== 6. split() ― 공백 기준으로 나누기 ==========")

# ---------------------------------------------------------------------------
# 형식:  문자열.split()          ← 괄호를 비우면 공백 기준
#        문자열.split("구분자")   ← 구분자를 넣으면 그 기준
#
# 구분자가 나오는 자리마다 잘라 '리스트'로 돌려준다. 구분자 자체는 사라진다.
# 슬라이싱은 '위치'로 자르고, split 은 '구분자'로 나눈다.
# ---------------------------------------------------------------------------

s = "PUMP A 03"
print(s.split())                      # → ['PUMP', 'A', '03']

# 공백이 여러 칸이어도 알아서 단어만 나눈다
menu = "에스프레소   아메리카노  카페라테"
print(menu.split())                   # → ['에스프레소', '아메리카노', '카페라테']


print()
print("========== 7. split(',') ― 구분자로 나누고 번호로 꺼내기 ==========")

s = "a,b,c,d"
print(s.split(","))                   # → ['a', 'b', 'c', 'd']   (쉼표는 사라짐)

# split 결과는 리스트 → 번호로 원하는 조각을 꺼낸다
date = "2025-01-15"
parts = date.split("-")
print(parts)                          # → ['2025', '01', '15']
print(parts[0])                       # → 2025   (연)
print(parts[1])                       # → 01     (월)
print(parts[2])                       # → 15     (일)
# 03_02 에서 슬라이싱으로 자리를 세던 것보다 훨씬 편하다


