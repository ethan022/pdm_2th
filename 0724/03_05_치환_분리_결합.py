print("========== 14. replace vs split 선택 기준 ==========")

# ---------------------------------------------------------------------------
#   Q. 언제 replace?  →  바꾸거나 지울 때        (결과는 '하나의 문자열')
#   Q. 언제 split?    →  나눠서 일부를 골라 쓸 때  (결과는 '리스트')
#   기준: 바꿔서 하나로 둘지 / 나눠서 따로 쓸지
#
#   메서드            용도                   결과
#   ------------------------------------------------
#   replace(A, B)    A를 B로 바꾸기·제거      문자열
#   split()          공백으로 나누기          리스트
#   split(",")       구분자로 나누기          리스트
#   "구분자".join()   조각 합치기             문자열
#
#   공통 원칙: 원본은 안 바뀜 → 결과를 변수에 다시 받기
#   텍스트 정리 기본 3단계: 나누기(split) → 정리(strip/lower) → 합치기(join)
# ---------------------------------------------------------------------------

# 같은 데이터, 다른 목적
date = "2025-01-15"
print(date.replace("-", ""))          # → 20250115           (하이픈 지워 하나로)
print(date.split("-"))                # → ['2025', '01', '15'] (나눠서 따로 쓰기)


