print("========== 4. replace 활용 ― 표기 통일과 숫자 변환 ==========")

# 용도 1) 표기 통일
status = "fault 발생"
print(status.replace("fault", "고장"))     # → 고장 발생

# 용도 2) 기호를 제거해 계산 가능한 형태로
s = "3,000"
print(type(s))                        # → <class 'str'>   (쉼표 때문에 아직 글자!)
# print(int(s))                       → ValueError: invalid literal for int()
n = int(s.replace(",", ""))           # 쉼표 제거 후 변환
print(n)                              # → 3000
print(type(n))                        # → <class 'int'>
print(n + 500)                        # → 3500   (이제 계산이 된다)


