print("========== 10. join() ― 조각 합치기 ==========")

# ---------------------------------------------------------------------------
# 형식:  "구분자".join(리스트)     ← 순서가 독특! 구분자가 '앞'에 온다
# 리스트의 조각들 사이에 구분자를 끼워 하나의 문자열로 합친다.
# ---------------------------------------------------------------------------

parts = ["2025", "01", "15"]
print("-".join(parts))                # → 2025-01-15
print("/".join(parts))                # → 2025/01/15
print("".join(parts))                 # → 20250115    (빈 값이면 딱 붙음)
print(", ".join(["사과", "배", "감"]))  # → 사과, 배, 감

# 주의: join 은 '문자열 조각'만 합칠 수 있다. 숫자가 섞이면 오류.
# print("-".join(["2025", 1, 15]))    → TypeError
#   → 숫자는 str() 로 먼저 문자열로 바꿔야 한다


print()
print("========== 11. print 의 sep 과 end ==========")

# ---------------------------------------------------------------------------
# sep : 여러 값 사이에 넣을 구분자 (기본값은 공백 한 칸)
# end : 출력 끝에 붙일 문자 (기본값은 줄바꿈)
# join 은 '리스트를 문자열로 합침' / sep 은 '출력할 때 사이에 넣음'
# ---------------------------------------------------------------------------

print(2025, 1, 15)                    # → 2025 1 15      (기본 sep = 공백)
print(2025, 1, 15, sep="-")           # → 2025-1-15
print(2025, 1, 15, sep=", ")          # → 2025, 1, 15

print("온도", end=" : ")               # end 를 바꾸면 줄바꿈 대신 그 문자가 붙음
print(25)                             # → 온도 : 25      (한 줄에 이어짐)


print()
print("========== 12. split 과 join 은 짝꿍 ==========")

# ---------------------------------------------------------------------------
#   split : 문자열 → 리스트      join : 리스트 → 문자열   (정반대!)
#   문법도 반대 방향:  문자열.split("구분자")   /   "구분자".join(리스트)
#   둘을 묶으면 '구분자를 통째로 교체'할 수 있다.
# ---------------------------------------------------------------------------

text = "사과,배,감"
parts = text.split(",")               # 쉼표로 나누고
joined = " / ".join(parts)            # " / " 로 다시 합침
print(joined)                         # → 사과 / 배 / 감

raw = "2025/01/15"
parts = raw.split("/")                # ['2025', '01', '15']
print("-".join(parts))                # → 2025-01-15    (/ 를 - 로 교체)


print()
print("========== 13. strip + split 함께 쓰기 ==========")

# ---------------------------------------------------------------------------
# 파일에서 읽은 줄은 앞뒤에 공백·줄바꿈이 붙어 있기 일쑤.
# strip 먼저 → split 나중 순서로 깔끔하게 나눈다.
#     text.strip().split(",")
# 파일 한 줄을 다룰 때 거의 항상 이 순서다.
# ---------------------------------------------------------------------------

raw = "  1,EQP-001,정상  "
parts = raw.strip().split(",")
print(parts)                          # → ['1', 'EQP-001', '정상']

# 나눈 '각 조각'에도 공백이 남아 있을 수 있다 → 조각을 꺼내 따로 정리
raw = "사과, 배 , 감"
parts = raw.split(",")
print(parts)                          # → ['사과', ' 배 ', ' 감']   (배·감 앞뒤 공백!)
print(parts[1].strip())               # → 배     (꺼낸 조각을 strip 으로 정리)

# 실전 패턴: CSV 한 줄에서 값 꺼내 정리하기
raw = "1, NORMAL ,25.3"
parts = raw.split(",")
print(parts)                          # → ['1', ' NORMAL ', '25.3']
status = parts[1].strip().lower()     # 공백 제거 → 소문자
print(status)                         # → normal


