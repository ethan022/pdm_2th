print("========== 1. 사칙연산 ==========")

# 연산자 = 값으로 계산하거나 비교하는 기호.
# 산술 연산은 '계산', 비교·논리 연산은 '판단'.
print(3 + 5)                  # → 8
print(10 - 4)                 # → 6
print(7 - 10)                 # → -3      (음수도 그대로)
print(4 * 5)                  # → 20
print(20 / 4)                 # → 5.0     ← 나눗셈은 딱 떨어져도 실수(float)


print()
print("========== 2. 나눗셈 결과는 항상 실수 ==========")

print(10 / 2)                 # → 5.0     (5가 아니라 5.0)
print(7 / 2)                  # → 3.5
print(type(10 / 2))           # → <class 'float'>


print()
print("========== 3. 몫(//) 과 나머지(%) ==========")

# ---------------------------------------------------------------------------
#   //  몫    → 나눈 결과에서 소수점 버림 (정수)
#   %   나머지 → 나누고 남은 값
# 시간 변환, 짝수/홀수 판별 등에 자주 쓴다.
# ---------------------------------------------------------------------------
print(7 // 2)                 # → 3       (몫)
print(7 % 2)                  # → 1       (나머지)
print(10 // 3)                # → 3
print(10 % 3)                 # → 1

# 활용 : 총 500분을 시:분으로 변환
minutes = 500
print(minutes // 60)          # → 8       (시간)
print(minutes % 60)           # → 20      (분)
print("가동 시간:", minutes // 60, "시간", minutes % 60, "분")
# → 가동 시간: 8 시간 20 분


print()
print("========== 4. 거듭제곱(**) ==========")

print(2 ** 3)                 # → 8       (2를 3번 곱함)
print(5 ** 2)                 # → 25
print(17 ** 5)                # → 1419857


print()
print("========== 5. 연산자 우선순위 ==========")

# ---------------------------------------------------------------------------
# 우선순위 :  **  >  *  /  //  %  >  +  -
# 수학과 똑같이 곱하기·나누기를 먼저 한다.
# 헷갈리면 괄호를 쓰면 된다. 괄호가 항상 1순위.
# ---------------------------------------------------------------------------
print(2 + 3 * 4)              # → 14      (3*4 먼저 → 2+12)
print((2 + 3) * 4)            # → 20      (괄호 먼저 → 5*4)
print(10 - 2 * 3)             # → 4
print(2 ** 3 + 1)             # → 9       (거듭제곱 먼저 → 8+1)


