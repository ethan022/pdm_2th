print("========== 6. 계산 결과를 변수에 저장 ==========")

result = 3 + 5
print(result)                 # → 8

avg = (80 + 90) / 2
print(avg)                    # → 85.0


print()
print("========== 7. 복합 할당 연산자 ==========")

# ---------------------------------------------------------------------------
#   score += 10   은   score = score + 10   의 줄임말
#   -=  *=  /=  도 같은 방식
# ---------------------------------------------------------------------------
score = 100
score += 10
print(score)                  # → 110

# 활용 : 재고 추적
stock = 100
print(stock)                  # → 100
stock += 50                   # 입고
print(stock)                  # → 150
stock -= 30                   # 출고
print(stock)                  # → 120
stock += 5                    # 반품
print(stock)                  # → 125


print()
print("========== 8. 문자열 연산 ==========")

# +  연결 (이어 붙이기)
print("안녕" + "하세요")        # → 안녕하세요
print("센서" + "A")            # → 센서A

# *  반복
print("하" * 3)                # → 하하하
print("-" * 10)                # → ----------
print("=" * 20)                # → ====================  (구분선 만들 때 유용)

# 숫자 + 문자열은 오류 (주석 처리)
# print(3 + "5")              → TypeError: unsupported operand type(s)
#   → 해결: 숫자를 str() 로 감싸기
print("횟수: " + str(5))       # → 횟수: 5


print()
print("========== 9. 비교 연산자 ==========")

# ---------------------------------------------------------------------------
# 두 값을 견주어 참·거짓을 낸다. 결과는 항상 True 또는 False (bool).
#   ==  같다        !=  다르다
#   >   크다        <   작다
#   >=  이상        <=  이하
#
#   주의: =  는 저장,  ==  는 비교!
# ---------------------------------------------------------------------------
print(5 == 5)                 # → True
print(5 != 3)                 # → True
print(7 > 10)                 # → False
print(7 < 10)                 # → True
print(5 >= 5)                 # → True    (같아도 참)
print(4 <= 3)                 # → False

# 비교 결과는 bool 자료형
result = 5 > 3
print(result)                 # → True
print(type(result))           # → <class 'bool'>

# 문자열도 비교할 수 있다
print("정상" == "정상")        # → True
print("A" == "a")             # → False   (대소문자는 다른 글자)
print("정상" == "정상 ")       # → False   (뒤 공백 하나 차이도 다른 값!)


print()
print("========== 10. 논리 연산자 ==========")

# ---------------------------------------------------------------------------
#   and  : 둘 다 참일 때만 True   ("그리고")
#   or   : 하나라도 참이면 True   ("또는")
#   not  : 참·거짓을 뒤집음       ("아니다")
# ---------------------------------------------------------------------------
print(True and True)          # → True
print(True and False)         # → False
print(True or False)          # → True
print(False or False)         # → False
print(not True)               # → False
print(not False)              # → True

# 비교와 조합하기
print(85 > 80 and 85 < 100)   # → True

temp = 85
print(temp > 80 and temp < 100)   # → True    (80 초과이면서 100 미만인가?)
print(not (temp > 90))            # → True    (90보다 크지 '않다'는 게 맞는가?)


