print("========== 11. 실전 : 설비 판정 ==========")

# 온도가 정상 범위(60 이상 90 이하)인가?
temp = 85
temp_ok = 60 <= temp and temp <= 90
print("온도 정상:", temp_ok)          # → 온도 정상: True

# 압력이 정상 범위(3 이상 7 이하)인가?
pressure = 5
pres_ok = 3 <= pressure and pressure <= 7
print("압력 정상:", pres_ok)          # → 압력 정상: True

# 둘 다 정상인가?
print("모두 정상:", temp_ok and pres_ok)   # → 모두 정상: True

# 하나라도 이상이면?
print("이상 있음:", not (temp_ok and pres_ok))   # → 이상 있음: False


print()
print("========== 12. 실전 : 설비 지표 계산 ==========")

# 불량률(%) = 불량 / 전체 * 100
total = 500
defect = 23
print("불량률(%):", defect / total * 100)     # → 불량률(%): 4.6

# 가동률(%) = 가동 시간 / 전체 시간 * 100
run_h = 21
all_h = 24
print("가동률(%):", run_h / all_h * 100)      # → 가동률(%): 87.5

# 평균
x = 80
y = 91
z = 90
print("평균:", (x + y + z) / 3)               # → 평균: 87.0

# 넓이 · 부피
side = 7
print("정사각형 넓이:", side ** 2)             # → 정사각형 넓이: 49
print("직육면체 부피:", 3 * 4 * 5)             # → 직육면체 부피: 60


