print("========== 2. input() 이 필요한 이유 ==========")

# ---------------------------------------------------------------------------
# 지금까지는 값을 우리가 코드에 직접 적었다.
# input() 은 사용자가 값을 직접 넣게 해준다.
# → 프로그램 하나로 여러 상황을 처리할 수 있게 됨.
#
# 형식:  변수 = input("안내 문구: ")
#        괄호 안의 안내 문구는 화면에 보여주고, 사용자가 엔터를 칠 때까지 기다린다.
# ---------------------------------------------------------------------------
#
#   name = input("이름: ")
#   print(name)
#
# 실행하면 화면에      이름:
# 사용자가 홍길동 입력  →  print 결과는  홍길동


print("========== 3. input() 의 결과는 '항상' 문자열 ==========")

# ---------------------------------------------------------------------------
# 이 단원에서 가장 중요한 한 줄:
#
#        input() 으로 받은 값은 숫자를 입력해도 '글자(str)' 다.
#
# 사용자가 25 를 입력해도 파이썬은 "25" 라는 글자로 받는다.
# 그래서 그대로 계산하면 이상한 결과가 나온다.
# ---------------------------------------------------------------------------

# input() 이 25 를 돌려준 상황을 그대로 흉내내 보자 (따옴표가 있는 것에 주목)
age_input = "25"                  # ← 이것이 input("나이: ") 의 실제 결과
print(age_input)                  # → 25              (화면엔 숫자처럼 보임)
print(type(age_input))            # → <class 'str'>   (하지만 실제로는 글자!)

# 글자끼리 더하면 계산이 아니라 '이어 붙이기'가 된다
a_input = "3"                     # input("수1: ") 결과
b_input = "5"                     # input("수2: ") 결과
print(a_input + b_input)          # → 35   ← 8이 아니다!

# print(a_input - b_input)        → TypeError  (글자는 뺄셈 자체가 불가능)


print()
print("========== 4. 형변환 : int() / float() / str() ==========")

# ---------------------------------------------------------------------------
#   int(값)    글자 → 정수    나이 · 개수 · 횟수
#   float(값)  글자 → 실수    온도 · 평균 · 측정값
#   str(값)    숫자 → 글자    문자열과 이어 붙일 때
# ---------------------------------------------------------------------------

# (1) int() 로 정수 변환
age = int(age_input)              # "25" → 25
print(age)                        # → 25
print(type(age))                  # → <class 'int'>
print(age + 1)                    # → 26   (이제 계산이 된다!)

# (2) 변환한 뒤 계산하기
a = int(a_input)
b = int(b_input)
print("합:", a + b)                # → 합: 8      (35가 아니라 8)
print("나눗셈:", a / b)            # → 나눗셈: 0.6

# (3) float() 로 실수 변환
height_input = "173.5"            # input("키: ") 결과
height = float(height_input)
print(height)                     # → 173.5
print(type(height))               # → <class 'float'>

# (4) str() 로 문자열 변환
age = 25
# print("나이: " + age)           → TypeError  (글자 + 숫자는 불가)
print("나이: " + str(age))         # → 나이: 25
print("나이:", age)                # → 나이: 25   (쉼표를 쓰면 str() 없이도 OK)


print()
print("========== 5. 형변환 주의점 ==========")

# ---------------------------------------------------------------------------
# (1) int("25.5") 는 오류!  소수점이 있는 글자는 int() 로 바로 못 바꾼다.
#       → 소수가 있을 수 있으면 float() 로 받는다.
#
# (2) 숫자가 아닌 글자를 변환하면 ValueError.
#       int("스물다섯")  →  ValueError: invalid literal for int() with base 10
#
# (3) int(input("...")) 은 괄호가 두 겹.
#       여는 괄호 두 개면 닫는 괄호도 두 개!
# ---------------------------------------------------------------------------

# 오류 예시 (주석 처리 — # 을 지워서 직접 확인해볼 것)
# print(int("25.5"))              → ValueError: invalid literal for int()
# print(int("스물다섯"))            → ValueError

# 올바른 방법
print(float("25.5"))              # → 25.5
print(int(float("25.5")))         # → 25    (실수로 바꾼 뒤 정수로 → 소수점 버림)


print()
print("========== 6. 입력 → 변환 → 계산 → 출력 4단계 ==========")

# ---------------------------------------------------------------------------
#   입력(input)  →  변환(int·float)  →  계산(연산)  →  출력(print)
#
#   가장 중요한 건 '변환' 단계. 안 하면 오류가 나거나 이상한 결과가 나온다.
#   막히면 "지금 어느 단계에서 막혔지?" 하고 스스로 물어볼 것.
# ---------------------------------------------------------------------------

# 예시 A) 태어난 해로 나이 구하기
birth_input = "1999"                       # ① 입력 (input 결과를 흉내)
birth_year = int(birth_input)              # ② 변환
age = 2026 - birth_year                    # ③ 계산
print("나이:", age, "세")                   # ④ 출력 → 나이: 27 세

# 예시 B) 온도를 받아 기준과 비교
temp_input = "85"                          # ① 입력
temp = int(temp_input)                     # ② 변환
print("80 초과?", temp > 80)                # ③④ 계산+출력 → 80 초과? True
print("0 이상?", temp >= 0)                 # → 0 이상? True

# 예시 C) 두 점수의 평균을 구해 기준과 비교
x = int("70")
y = int("50")
avg = (x + y) / 2
print("평균:", avg)                         # → 평균: 60.0
print("60 이상?", avg >= 60)                # → 60 이상? True

# 예시 D) 설비 정보 여러 개
device_name = "펌프A"                       # input("설비명: ")
device_temp = float("25.5")                # float(input("온도: "))
check_count = int("12")                    # int(input("점검 횟수: "))
print("설비:", device_name)                 # → 설비: 펌프A
print("온도:", device_temp, "도")            # → 온도: 25.5 도
print("점검:", check_count, "회")            # → 점검: 12 회


