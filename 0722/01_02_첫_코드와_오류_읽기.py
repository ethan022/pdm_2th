#  배운 것 : print(), 숫자와 문자의 차이, 여러 값 출력, 계산, 주석,
#            오류 메시지 읽는 법 (NameError / SyntaxError)
#  사용법  : 위에서부터 한 줄씩 읽고, "→" 결과를 먼저 예상한 뒤 실행해서 맞춰보기
# =============================================================================

print("========== 1. print 기본 ==========")

# ---------------------------------------------------------------------------
# print 는 파이썬에 기본으로 들어있는 함수(내장 함수).
# 함수 = 일해주는 도구,  괄호 = 재료를 넣는 입구
# 괄호 안의 내용을 화면에 출력한다. print 한 번 = 한 줄.
# ---------------------------------------------------------------------------
print("안녕하세요")            # → 안녕하세요
print("첫 번째 줄")            # → 첫 번째 줄
print("두 번째 줄")            # → 두 번째 줄


print()
print("========== 2. 숫자와 문자의 차이 ==========")

# ---------------------------------------------------------------------------
# 규칙 한 쌍만 기억:  숫자는 따옴표 없이,  글자는 따옴표로.
#   · 따옴표 없는 숫자 → 계산할 수 있는 '진짜 값'
#   · 따옴표를 붙이면  → 숫자 모양의 '글자'
# ---------------------------------------------------------------------------
print(75)                     # → 75      (온도. 계산 가능한 숫자)
print(2.3)                    # → 2.3     (진동. 소수점도 그대로)
print("75")                   # → 75      (화면엔 똑같아 보이지만 '글자')

# 같은 75라도 따옴표가 있으면 글자, 없으면 숫자.
# 설비 측정값은 계산해야 하니까 따옴표 없이,
# 항목 이름이나 설명은 글자니까 따옴표로.


print()
print("========== 3. 여러 값 함께 출력 ==========")

# ---------------------------------------------------------------------------
# 괄호 안에 쉼표(,)로 나누면 한 줄에 여러 값.  쉼표 사이엔 자동으로 띄어쓰기가 들어감.
# print(75) 만 보면 무슨 값인지 모른다 → 이름표를 함께 붙이자.
# ---------------------------------------------------------------------------
print("온도", 75)              # → 온도 75
print("온도(℃):", 75)          # → 온도(℃): 75
print("진동(mm/s):", 2.3)      # → 진동(mm/s): 2.3

# 점검 화면처럼 구성해보기
print("=== 1번 설비 측정값 ===")   # → === 1번 설비 측정값 ===
print("온도(℃):", 75)             # → 온도(℃): 75
print("진동(mm/s):", 2.3)         # → 진동(mm/s): 2.3
print("압력(bar):", 4.0)          # → 압력(bar): 4.0


print()
print("========== 4. 계산 결과 출력 ==========")

# ---------------------------------------------------------------------------
# 괄호 안에 계산식을 넣으면 계산을 먼저 하고 그 결과를 출력한다.
# 계산 기호 :  더하기 +   빼기 -   곱하기 *   나누기 /
# ---------------------------------------------------------------------------
print(75 + 5)                          # → 80
print(82 - 75)                         # → 7
print("두 측정값의 합:", 75 + 80)        # → 두 측정값의 합: 155
print("온도 변화량:", 82 - 75)           # → 온도 변화량: 7
print("평균 온도:", (75 + 80) / 2)       # → 평균 온도: 77.5

# 주의: 괄호를 빼면 결과가 달라진다.  75 + 80 / 2 는 80/2 를 먼저 계산함
print("괄호 없이:", 75 + 80 / 2)         # → 괄호 없이: 115.0


print()
print("========== 5. 주석 ==========")

# ---------------------------------------------------------------------------
# 샵(#) 뒤에 적은 내용은 컴퓨터가 실행하지 않는다. 오직 사람이 읽는 메모.
#   · 줄 끝에 # 설명   → 그 줄에만 메모 추가
#   · 줄 맨 앞에 #     → 그 줄 전체를 잠시 꺼 둠
# 단축키 Ctrl + /  로 선택한 줄을 한 번에 주석 처리/해제할 수 있다.
# ---------------------------------------------------------------------------
# 1번 설비 측정값 출력
print("온도(℃):", 82)      # 정상 71 대비 상승   ← 이 부분은 실행 안 됨
# print("이 줄은 실행되지 않습니다")

# 주석을 달아도 결과는 바뀌지 않는다. 바뀌는 건 '나중에 읽는 사람의 이해도'뿐.


print()
print("========== 6. print 로 중간값 찍어 확인하기 ==========")

# ---------------------------------------------------------------------------
# 계산이 이상할 때는 중간값을 하나씩 print 로 찍어보면 어디서 틀렸는지 바로 보인다.
# 이건 앞으로도 계속 쓰게 될 가장 기본적인 디버깅 방법이다.
# ---------------------------------------------------------------------------
print("정상 온도:", 71)              # → 정상 온도: 71
print("현재 온도:", 82)              # → 현재 온도: 82
print("차이 계산 직전:", 82, 71)      # → 차이 계산 직전: 82 71   (중간값 확인)
print("온도 상승량:", 82 - 71)        # → 온도 상승량: 11


print()
print("========== 7. 오류 읽기 ==========")

# ---------------------------------------------------------------------------
# 빨간 오류 메시지는 혼내는 게 아니라 '안내문'이다.
# 길어 보여도 볼 것은 딱 세 가지:
#     위치 (line 3)  ·  종류 (NameError)  ·  설명 (not defined)
#
# 아래 예시들은 실행하면 프로그램이 멈추므로 주석 처리해 두었다.
# 하나씩 # 을 지워서 직접 오류를 만들어 보고, 메시지를 읽고, 다시 주석 처리할 것.
# ---------------------------------------------------------------------------

# --- (1) NameError : 정의한 적 없는 이름을 썼을 때 -------------------------
# 가장 흔한 원인은 '글자에 따옴표를 빠뜨린 것'
#
# print(온도)
#   → NameError: name '온도' is not defined
#   → 고치는 법: 따옴표를 붙인다
print("온도")                  # → 온도   (이렇게 고치면 정상)

# --- (2) SyntaxError : 문법이 어긋났을 때 ---------------------------------
# 흔한 원인은 따옴표나 괄호의 '짝'이 안 맞는 것
#
# print("온도)          ← 따옴표 짝이 안 맞음
# print("진동"          ← 괄호 짝이 안 맞음
#   → SyntaxError: invalid syntax  또는  unterminated string literal
#   → 고치는 법: 여는 게 있으면 닫는 것도 있어야 한다
print("진동")                  # → 진동   (짝을 맞추면 정상)

# --- (3) 디버깅 연습 : 오류 3개가 섞인 코드 -------------------------------
# 아래 세 줄에는 오류가 하나씩 들어 있다. 어디가 틀렸는지 먼저 찾아보자.
#
# print(온도, 75)          ← 온도에 따옴표 없음        (NameError)
# print("진동", 2.3        ← 닫는 괄호 없음            (SyntaxError)
# print("압력": 4.0)       ← 쉼표 자리에 콜론          (SyntaxError)
#
# ↓ 고친 코드
print("온도", 75)              # → 온도 75
print("진동", 2.3)             # → 진동 2.3
print("압력", 4.0)             # → 압력 4.0

# 오류 대처 4단계
#   1) 당황 안 하기      2) 종류·위치 읽기
#   3) 해당 줄 확인      4) 고치고 다시 실행


print()
print("========== 8. 종합 : 설비 점검 리포트 ==========")

# 오늘 배운 출력 기술(제목 / 이름표+값 / 계산 / 주석)을 하나로 합친 리포트
print("===== 1번 설비 점검 =====")
print("온도(℃):", 82)                  # 정상 71 대비 상승
print("진동(mm/s):", 2.3)              # 정상 범위
print("온도 상승량:", 82 - 71)          # → 11
print("========================")


