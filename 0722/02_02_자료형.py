print("========== 11. 종합 : 값을 바꾸면 자료형도 바뀐다 ==========")

n = 3
print(n, type(n))             # → 3 <class 'int'>
n = 3.0                       # 소수점을 붙이면
print(n, type(n))             # → 3.0 <class 'float'>
n = "3"                       # 따옴표를 붙이면
print(n, type(n))             # → 3 <class 'str'>

# 이름에 맞는 자료형 골라서 저장하기
device_temp = 25.5            # float — 온도는 소수점이 있을 수 있음
