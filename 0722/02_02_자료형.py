print("========== 10. 자료형 구분하는 순서 ==========")

# ---------------------------------------------------------------------------
#   Q1. 따옴표가 있나?            → 있으면 무조건 str
#   Q2. True / False 인가?        → 맞으면 bool
#   Q3. 소수점이 있나?            → 있으면 float, 없으면 int
#
#   int   정수    3        개수 · 나이
