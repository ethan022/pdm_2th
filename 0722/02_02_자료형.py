print("========== 1. 왜 자료형을 알아야 하나 ==========")

# ---------------------------------------------------------------------------
# 값 하나하나가 '객체'이고, 그 값의 종류가 '자료형'이다.
# 종류에 따라 할 수 있는 일이 다르다.  (숫자에는 '대문자로 바꾸기' 기능이 없다)
# 그래서 데이터를 다루려면 종류부터 알아야 한다.
# ---------------------------------------------------------------------------


print("========== 2. int  정수 ==========")

# 소수점 없는 숫자. 개수 · 나이 · 횟수처럼 딱 떨어지는 값.
count = 3
age = 27
zero = 0
print(count, age, zero)       # → 3 27 0


print()
print("========== 3. float  실수 ==========")

# 소수점이 있는 숫자. 온도 · 평균 · 진동값처럼 정밀한 값.
temp = 25.5
height = 173.5
print(temp, height)           # → 25.5 173.5

# 소수점만 붙여도 자료형이 달라진다
a = 5                         # int
b = 5.0                       # float
print(a)                      # → 5
print(b)                      # → 5.0     ← 5가 아니라 5.0으로 나옴


print()
print("========== 4. str  문자열 ==========")

# 따옴표로 감싼 글자. 이름 · 상태 라벨 · 코드.
name = "센서A"
empty = ""                    # 빈 문자열 (글자 0개)
print(name)                   # → 센서A
print(empty)                  # →         (아무것도 안 나옴, 빈 줄)

# 따옴표 규칙
#   · 큰따옴표("")와 작은따옴표('') 둘 다 가능
#   · 여는 것과 닫는 것의 종류가 같아야 함
#   · 글자 안에 따옴표가 있으면 바깥은 다른 종류로
print("It's normal")          # → It's normal      (안에 ' 이 있으니 바깥은 ")
print('그는 "정상"이라 했다')     # → 그는 "정상"이라 했다  (안에 " 이 있으니 바깥은 ')

# 숫자처럼 보이지만 문자열인 것 (아주 중요)
n1 = 3                        # 숫자
n2 = "3"                      # 글자
print(n1)                     # → 3
print(n2)                     # → 3       ← 화면엔 똑같아 보인다!


print()
print("========== 5. bool  불린 ==========")

# ---------------------------------------------------------------------------
# 참과 거짓을 나타내는 자료형. 값은 True 와 False 딱 둘뿐.
# 켜짐/꺼짐, 정상/비정상 같은 두 가지 상태에 사용한다.
# 반드시 첫 글자 대문자!  true / false 는 없는 이름 → NameError
# ---------------------------------------------------------------------------
ok = True
no = False
print(ok, no)                 # → True False

# 실수 예시 (주석 처리)
# a = true                    → NameError: name 'true' is not defined
b = "True"                    # 이건 bool 이 아니라 '글자'다! (따옴표 주의)
print(b)                      # → True    ← 화면엔 똑같아 보이지만 str

# bool 은 '비교의 결과'로 자주 만들어진다
print(3 > 2)                  # → True
print(5 == 5)                 # → True
print(1 > 10)                 # → False


print()
print("========== 6. type() 으로 종류 확인하기 ==========")

# 형식:  type(값)   또는   type(변수)
print(type(25))               # → <class 'int'>
print(type(25.5))             # → <class 'float'>
print(type("센서A"))           # → <class 'str'>
print(type(True))             # → <class 'bool'>

# 변수에 담긴 값도 똑같이 확인 가능
count = 3
temp = 25.5
name = "센서A"
is_ok = True
print(type(count))            # → <class 'int'>
print(type(temp))             # → <class 'float'>
print(type(name))             # → <class 'str'>
print(type(is_ok))            # → <class 'bool'>

# 값과 종류를 한 번에 보기
print(count, type(count))     # → 3 <class 'int'>


print()
print("========== 7. 자료형마다 동작이 다르다 ==========")

# ---------------------------------------------------------------------------
# 같은 + 기호라도 자료형에 따라 하는 일이 완전히 다르다.
#   숫자 + 숫자   → 더하기(계산)
#   글자 + 글자   → 이어 붙이기(연결)
# ---------------------------------------------------------------------------
print(3 + 5)                  # → 8         (계산)
print("3" + "5")              # → 35        (이어 붙이기!)
print("10" + "20")            # → 1020      (1020이지 30이 아니다)
print("안녕" + "하세요")        # → 안녕하세요

# 숫자와 글자를 그냥 더하면 오류 (주석 처리)
# print(3 + "5")              → TypeError: unsupported operand type(s) for +: 'int' and 'str'
# print("123" + 1)            → TypeError
#   → 해결: 종류를 맞춰준다.
#     str()·int() 로 종류를 바꾸는 것을 '형변환'이라 하고, 02_04에서 자세히 배운다.
print(str(3) + "5")           # → 35        (숫자 3을 글자 "3"으로 맞춤)
print(3 + int("5"))           # → 8         (글자 "5"를 숫자 5로 맞춤)


print()
print("========== 8. 자료형 확인이 중요한 이유 ==========")

# ---------------------------------------------------------------------------
# 의도한 종류와 실제 종류가 다르면 문제가 생긴다.
# 계산이 이상하면 추측하지 말고 두 단계로 점검:
#       print(값)  →  값 확인
#       type(값)   →  종류 확인
# 확인은 몇 초, 안 해서 찾는 오류는 몇 분.
# ---------------------------------------------------------------------------
mystery = "123"
print(mystery)                # → 123              (숫자처럼 보임)
print(type(mystery))          # → <class 'str'>    (실제로는 글자!)


print()
print("========== 9. 알아두면 좋은 두 가지 ==========")

# (1) 실수 계산에는 작은 오차가 생길 수 있다
print(0.1 + 0.2)              # → 0.30000000000000004   (0.3이 아니다!)
print(round(0.1 + 0.2, 2))    # → 0.3                   (round 로 자릿수를 맞춤)

# (2) 나눗셈(/)은 딱 떨어져도 결과가 항상 실수(float)
print(3 + 5)                  # → 8      (int)
print(6 * 7)                  # → 42     (int)
print(10 / 2)                 # → 5.0    (float!)
print(type(10 / 2))           # → <class 'float'>


