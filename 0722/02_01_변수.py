print("========== 6. 변수에 다른 변수 담기 ==========")

# ---------------------------------------------------------------------------
# b = a 는 '그 순간의 값'을 복사하는 것이지, 두 변수를 하나로 묶는 게 아니다.
# 그래서 나중에 a 가 바뀌어도 b 는 그대로다.
# ---------------------------------------------------------------------------
a = 100
b = a                     # 지금 a의 값(100)을 b에 복사
a = 999                   # a만 바꿔도
print(a)                  # → 999
print(b)                  # → 100    ← 많은 사람이 999를 예상하지만 100이 정답


