print("========== 1. 변수와 할당 ==========")

# ---------------------------------------------------------------------------
# 형식:   변수이름 = 값
# 뜻  :   "오른쪽 값을 왼쪽 이름에 저장하라"  ← '같다'가 아니다!
# 변수는 값에 붙이는 이름표. 이름표는 그대로 두고 안의 값만 바꿀 수 있다.
# ---------------------------------------------------------------------------
temp = 25                 # 숫자 저장
name = "센서A"             # 글자는 따옴표로
count = 3                 # 숫자 저장

print(temp)               # → 25       (값을 꺼내 씀)
print(name)               # → 센서A
print(count)              # → 3

# 따옴표 유무의 차이 (아주 중요)
print(temp)               # → 25       (변수 temp 의 '값')
print("temp")             # → temp     (따옴표 → 그냥 글자)


print()
print("========== 2. = 는 '저장'이지 '같다'가 아니다 ==========")

# ---------------------------------------------------------------------------
# x = x + 5 가 수학적으로는 말이 안 되지만 파이썬에서는 오류가 아니다.
# 오른쪽(x + 5)을 먼저 계산해서, 그 결과를 다시 x에 저장하기 때문.
# '같은지' 비교하는 건 등호 두 개(==)  → 02_03 연산자에서 배움
# ---------------------------------------------------------------------------
x = 10
print(x)                  # → 10
x = x + 5                 # 지금 x(10) + 5 = 15 를 다시 x에 저장
print(x)                  # → 15
x = x * 2                 # 지금 x(15) * 2 = 30 을 다시 x에 저장
print(x)                  # → 30


