print("========== 7. 한 줄에 여러 변수 만들기 ==========")

# 형식:  이름1, 이름2, ... = 값1, 값2, ...    ← 왼쪽 개수 = 오른쪽 개수
width, height = 4, 3
print(width)              # → 4
print(height)             # → 3

p, q, r = 10, 20, 30
print(p, q, r)            # → 10 20 30

# 실수 예시 (개수가 다르면 오류)
# m, n = 1, 2, 3          → ValueError: too many values to unpack


