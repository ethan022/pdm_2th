print("========== 3. 코드는 위에서 아래로 ==========")

# 컴퓨터는 코드를 위에서 아래로 순서대로 읽는다. 그 순간의 값이 출력된다.
y = 10
print(y)                  # → 10
y = 20
print(y)                  # → 20

# 재할당을 여러 번 하면 '마지막에 저장한 값'만 남는다
score = 10
