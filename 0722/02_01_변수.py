print("========== 9. NameError 읽고 고치기 ==========")

# ---------------------------------------------------------------------------
# NameError 가 나는 두 가지 원인
#   원인 1 : 변수를 만들기 전에 사용함
#   원인 2 : 철자 · 대소문자가 다름  (score ≠ Score)
#
# 아래는 일부러 오류를 내는 코드다. # 을 지워서 직접 확인해보고 다시 주석 처리할 것.
# ---------------------------------------------------------------------------
# print(nothing)          → NameError: name 'nothing' is not defined

# score = 90
# print(Score)            → NameError: name 'Score' is not defined  (S 대문자!)

# ↓ 고친 코드
value = 25                # 먼저 만들고
print(value)              # → 25   그다음 사용

score = 90
print(score)              # → 90   (철자·대소문자를 맞춤)


