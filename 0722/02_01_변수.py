print("========== 4. 변수 이름 규칙 ==========")

# ---------------------------------------------------------------------------
# 지켜야 하는 규칙 (안 지키면 오류)
#   · 영문자 · 숫자 · 밑줄(_) 만 사용
#   · 숫자로 시작 불가        temp2 (O)   2temp (X → SyntaxError)
#   · 중간에 공백 불가        sensor count (X)
#   · if · for · True 같은 예약어는 사용 불가
#   · print · type 같은 함수 이름은 덮어쓰지 않기
#
# 지키면 좋은 스타일 (안 지켜도 돌아가지만 나쁜 코드)
#   · 이름만 봐도 무슨 값인지 드러나게      a  (X)  →  sensor_count  (O)
#   · 여러 단어는 밑줄로 잇고 소문자로      userName (X) → user_name (O)  = snake_case
# ---------------------------------------------------------------------------
sensor_count = 3          # 좋은 이름 (의미가 드러남 + snake_case)
temp2 = 25                # 가능 (숫자로 '시작'만 안 하면 됨)
device_temp = 25.5

print(sensor_count)       # → 3
print(temp2)              # → 25
print(device_temp)        # → 25.5

# 실수 예시 (실행하면 오류나므로 주석 처리)
# 2temp = 1               → SyntaxError: invalid decimal literal
# sensor count = 3        → SyntaxError  (중간 공백)


