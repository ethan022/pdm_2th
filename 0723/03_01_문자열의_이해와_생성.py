print("========== 1. 왜 문자열을 배우나 ==========")

# ---------------------------------------------------------------------------
# 설비 데이터는 숫자뿐 아니라 '글자 데이터'로 가득하다.
#   센서 이름   'temp_sensor_1', 'vibration_02'
#   상태 라벨   'NORMAL', '정상', 'WARNING'
#   설비 코드   'EQP-001', 'PUMP_A_03'
#   파일·컬럼명 'cmapss_fd001.csv', 'cycle'
#
# 문자열 정리는 숫자 분석 전에 반드시 거치는 필수 단계다.
# ---------------------------------------------------------------------------


print("========== 2. 문자열 만들기 ==========")

# 문자열(string) = 글자들이 순서대로 이어진 데이터.
# 만드는 법 = 글자를 따옴표로 감싸기.
# 'A' 한 글자도, '설비 점검 완료' 같은 문장도 모두 하나의 문자열.

name = '홍길동'
status = "정상"
code = 'EQP-001'
print(name, status, code)          # → 홍길동 정상 EQP-001

# 따옴표 안에는 한글 · 영문 · 숫자 · 기호 거의 모두 가능
mixed = "설비 EQP-001 온도 25.5도 (정상)"
print(mixed)                       # → 설비 EQP-001 온도 25.5도 (정상)

# 주의: '2025'(문자열) 와 2025(숫자) 는 종류가 다르다
print(type('2025'))                # → <class 'str'>
print(type(2025))                  # → <class 'int'>

# 전화번호는 계산할 값이 아니라 '글자로 된 번호'
phone = '010-1234-5678'
print(phone)                       # → 010-1234-5678


print()
print("========== 3. 따옴표 규칙 ==========")

# ---------------------------------------------------------------------------
#   · 작은따옴표('')와 큰따옴표("") 둘 다 가능
#   · 시작과 끝의 종류가 같아야 함
#   · 글자 안에 따옴표가 있으면 → 바깥은 다른 종류로!
# ---------------------------------------------------------------------------
a = '작은따옴표로 만든 문자열'
b = "큰따옴표로 만든 문자열"
print(a)                           # → 작은따옴표로 만든 문자열
print(b)                           # → 큰따옴표로 만든 문자열

# 안에 작은따옴표가 있으면 바깥은 큰따옴표
print("It's normal")               # → It's normal

# 안에 큰따옴표가 있으면 바깥은 작은따옴표
print('그는 "정상"이라고 말했다')     # → 그는 "정상"이라고 말했다

# 짝이 안 맞으면 SyntaxError (주석 처리)
# print('It's normal')             → SyntaxError  (중간의 ' 에서 문자열이 끝나버림)
# print("온도)                     → SyntaxError: unterminated string literal


print()
print("========== 4. 여러 줄 문자열 (삼중 따옴표) ==========")

# 따옴표를 세 개 쓰면 여러 줄을 줄바꿈 그대로 담을 수 있다.
notice = """설비 점검 안내
1. 전원 확인
2. 센서 점검"""
print(notice)
# → 설비 점검 안내
# → 1. 전원 확인
# → 2. 센서 점검

print("-" * 20)

check_list = """가동 점검표
1. 온도 확인
2. 압력 확인"""
print(check_list)
# → 가동 점검표
# → 1. 온도 확인
# → 2. 압력 확인


print()
print("========== 5. 이스케이프 문자 ==========")

# ---------------------------------------------------------------------------
# 역슬래시(\) 를 붙여 줄바꿈 · 탭 · 따옴표를 표현하는 방법을 '이스케이프'라 한다.
# 화면엔 두 글자처럼 보여도 컴퓨터에겐 하나의 동작이다.
#
#    \n    줄바꿈
#    \t    탭 (간격)
#    \\    역슬래시 그 자체
#    \"    큰따옴표
#    \'    작은따옴표
# ---------------------------------------------------------------------------

print("1줄\n2줄")
# → 1줄
# → 2줄

print("이름\t상태")                 # → 이름    상태     (탭 간격)
print("온도\t압력\t진동")            # → 온도    압력    진동

print("C:\\data\\sensor.csv")       # → C:\data\sensor.csv   (역슬래시 하나를 쓰려면 두 번)

print("그는 \"정상\"이라 했다")       # → 그는 "정상"이라 했다   (이스케이프로도 가능)

# \n 이 정말 '한 글자'인지 확인해보기
print(len("1줄\n2줄"))              # → 5   (1,줄,\n,2,줄 → \n 은 한 글자!)


print()
print("========== 6. 빈 문자열 vs 공백 문자열 ==========")

# ---------------------------------------------------------------------------
#   ''    빈 문자열   → 글자가 0개, 길이 0
#   ' '   공백 문자열 → 공백 글자 1개, 길이 1
#   컴퓨터에겐 완전히 다른 값이다. 데이터 정리할 때 자주 걸리는 함정.
# ---------------------------------------------------------------------------
empty = ""
space = " "
print(len(empty))                  # → 0
print(len(space))                  # → 1
print(empty == space)              # → False   (다른 값!)

# 눈으로 확인하기 어려우니 대괄호로 감싸서 확인하는 습관
print("[" + empty + "]")           # → []
print("[" + space + "]")           # → [ ]


print()
print("========== 7. 문자열과 숫자는 다르다 ==========")

# 같은 + 기호도 자료형에 따라 동작이 다르다
print(25 + 5)                      # → 30      (숫자: 합산)
print('25' + '5')                  # → 255     (글자: 연결)

# 글자 + 숫자는 오류 → str() 로 숫자를 글자로 바꿔준다
temp = 25
# print('온도: ' + temp)           → TypeError: can only concatenate str (not "int") to str
print('온도: ' + str(temp))         # → 온도: 25

# 글자를 int() 로 바꾸려 할 때도 주의
print(int('123'))                  # → 123     (숫자 형태의 글자는 변환 가능)
# print(int('온도'))                → ValueError (숫자가 아닌 글자는 변환 불가)


print()
print("========== 8. 문자열 흔한 오류 3가지 ==========")

# ---------------------------------------------------------------------------
#   1. 따옴표 짝 불일치      → SyntaxError    : 시작·끝 따옴표 확인
#   2. 글자 + 숫자           → TypeError      : 숫자를 str() 로 변환
#   3. 숫자 아닌 글자를 int() → ValueError     : 변환 불가
# ---------------------------------------------------------------------------


print()
print("========== 9. 실전 1 : 설비 라벨 이어 붙이기 ==========")

code = "PUMP_A"
state = "정상"
date = "2025-01-15"
print(code + " / " + state + " / " + date)
# → PUMP_A / 정상 / 2025-01-15

# 사이에 넣는 " / " 를 '구분자'라고 한다. 구분자에 따라 모양이 달라진다.
print(code + " - " + state + " - " + date)
# → PUMP_A - 정상 - 2025-01-15
print(code + " | " + state + " | " + date)
# → PUMP_A | 정상 | 2025-01-15


print()
print("========== 10. 실전 2 : 설비 정보 카드 ==========")

code = "PUMP_A"
state = "정상"
hours = 1200                        # 숫자!
day = "2025-01-15"

# \n 으로 줄을 나누고, 숫자 hours 는 str() 로 바꿔서 이어 붙인다
card = "설비: " + code + "\n상태: " + state + "\n가동: " + str(hours) + "시간\n점검: " + day
print(card)
# → 설비: PUMP_A
# → 상태: 정상
# → 가동: 1200시간
# → 점검: 2025-01-15

print()

# 같은 결과를 삼중 따옴표로 만들 수도 있다 (값이 고정일 때만)
card2 = """설비: PUMP_A
상태: 정상
가동: 1200시간
점검: 2025-01-15"""
print(card2)


